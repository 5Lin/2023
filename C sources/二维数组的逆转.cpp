#include<stdio.h>
int main(){
	int a[4][5]	=		//四行五列如下图所示 
	{
		{81,92,83,91,66},
		{61,67,86,98,66},
		{81,92,83,92,66},
		{91,72,73,93,66}
	};
	int i,j;
	for(i=0;i<4;i++)	//横纵互逆的话，i<5,j<4，且下面的i和j互换位置 
	{
	for(j=0;j<5;j++)
	{
	  printf("%d",a[i][j]);	//互逆的话，此处i,j换位置 
	}
	printf("\n");
	}
	return 0;
} 
